package org.teavm.flavour.templates.test;

import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author Alexey Andreev
 */
public class ModelPojo {
    private String name = "";
    private int weight;
    private List<Row> rows = new ArrayList<>();

    public ModelPojo(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public List<Row> getRows() {
        return rows;
    }

    public int getTotal() {
        int total = 0;
        for (Row row : rows) {
            total += row.getAmount();
        }
        return total;
    }

    public static class Row {
        private String name;
        private int amount;

        public Row(String name, int amount) {
            this.name = name;
            this.amount = amount;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getAmount() {
            return amount;
        }

        public void setAmount(int amount) {
            this.amount = amount;
        }
    }
}
